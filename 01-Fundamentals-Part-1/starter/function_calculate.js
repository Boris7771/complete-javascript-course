function calculate(x, y) {
  console.log(`${x} + ${y} = ${x + y}`);
  console.log(x + " + " + y + " = " + (x + y));
  console.log(`${x} - ${y} = ${x - y}`);
  console.log(x + " - " + y + " = " + (x - y));
  console.log(`${x} * ${y} = ${x * y}`);
  console.log(x + " * " + y + " = " + x * y);
  console.log(`${x} / ${y} = ${x / y}`);
  console.log(x + " / " + y + " = " + x / y);
  console.log(`${x} % ${y} = ${x % y}`);
  console.log(x + " % " + y + " = " + (x % y));
  console.log(`${x} ** ${y} = ${x ** y}`);
  console.log(x + " ** " + y + " = " + x ** y);
}
calculate(70, 6);
